    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        UsuarioDaoJDBC usuarioJDBC=new UsuarioDaoJDBC();
        Usuario us=new Usuario();
        String username=txtUsername.getText();
        String password=txtPassword.getText();
        us.setLogin(username);
        us.setPassword(password);
        try {
            if (username.equalsIgnoreCase("root") && password.equalsIgnoreCase("root")){
                FormMain m=new FormMain(us);
                m.setVisible(true);
                m.setLocationRelativeTo(null);
                this.dispose();
                    
            }else if (usuarioJDBC.buscarUsuario(username, password)){
                    FormReserva m=new FormReserva(us);
                    m.setVisible(true);
                    m.setLocationRelativeTo(null);
                    this.dispose();
                    }else{
                        JOptionPane.showMessageDialog(null, "Usuario no registrado");
                    }
        }
        catch (SQLException e) {
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

